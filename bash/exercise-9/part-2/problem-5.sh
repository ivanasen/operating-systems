#!/bin/bash

# Да се състави командна процедура, която получава при стратиране в командния
# ред акаунт на потребител. Процедурата изпраща съобщение на подадения потребител,
# съдържащо общия размер на всички файлове и директории на потребителска
# група KN./SIZE_ALL_KN/

usr=$1

total_size=$(find / -group "KN" -exec du -ch {} + | tail -n 1 | awk '{print $1}')
echo "$total_size" | write $usr

