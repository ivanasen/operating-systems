#!/bin/bash

# Да се състави командна процедура, която извежда информация за стойността
# и броя дефинирани позиционни параметри, прави файловете
# в текущата директория – позиционни параметри, изполва два пъти командата
# shift и пак извежда стойността и броя дефинирани парамерти.

params_info() {
    echo -n "param values: "
    echo $@
    echo -n "param count: "
    echo $#
}

params_info $@

set $(ls)

shift 2

params_info $@
