#!/bin/bash

# Да се състави командна процедура, която получава при стратиране в командния ред
# име на потребител. Процедурата на всеки 30 сек проверява дали потребителят е
# стартирал сесия, в който случай прекратява проверката, извеждайки на 
# стандартния изход подходящо съобщвние.

read usr

while true; do
    (users | grep "$usr" &> /dev/null) && break
    sleep 30
done

echo "User $usr has logged in!"
