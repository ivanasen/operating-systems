#!/bin/bash

# Да се състави командна процедура, която получава като параметри в командния ред
# име на директория и акаунт на потребител. Процедурата намира общия размер
# на всички файлове в подадената директория и изпраща получената информация 
# на потребителя с подадения акаунт, в случай че той е стратирал сесия.

dir=$1
usr=$2

size=$(du -sh $dir | cut -d $'\t' -f 1)
(users | grep "$usr" &>/dev/null) &&
    echo $size | write $usr

